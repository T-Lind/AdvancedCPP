#include "Numbers.h"

void number_s() {
    using namespace std;

    cout << "\nNUMBERS:\n" << endl;

    cout << "2^5 = " << pow(2, 5) << endl;
    cout << "sqrt(2) = " << sqrt(2) << endl;
    cout << "Round 3.5: " << round(3.5) << endl;
    cout << "Floor 3.9: " << floor(3.9) << endl;
    cout << "Ceiling 3.1: " << ceil(3.1) << endl;
    cout << "Max of 3 and 10.2: " << fmax(3, 10.2) << endl;
    cout << "Min of 3 and 10.2: " << fmin(3, 10.2) << endl;

}
